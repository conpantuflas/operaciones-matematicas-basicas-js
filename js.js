//OPERACIONES CON FUNCIONES JS

// resta
function resta(a,b){
    let resultado = a - b
    return resultado
}
console.log(resta(13,6))

//suma
function suma(a,b){
    let resultado = a + b
    return resultado
}
console.log(suma(6,9))

//division
function division(a,b){
    let resultado = a / b
    return resultado
}
console.log(division(60,8))

//nultiplicacion
function multiplicacion(a,b){
    let resultado = a * b
    return resultado
}
console.log(multiplicacion(5,2))